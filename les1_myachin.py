print(str('Давайте рассчитаем ежемесячный платеж по ипотеке.')) # просто немного полезности :) рассчет рабочий
price = int(input('Стоимость объекта: '))
pv = int(input('Первоначальный взнос: '))
rate = float(input('Процентная ставка: '))
time = int(input('Срок ипотеки: '))
s = price - pv # сумма кредита
m = time * 12 # кол-во месяцев
p = rate / 12 / 100 # ежемесячная ставка
koef = (p * (1 + p) ** m / ((1 + p) ** m - 1)) # коэффициент аннуитета
x = int(koef * s)
print(f'Ежемесячный платеж в рублях: {x}')

